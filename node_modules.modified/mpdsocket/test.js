var net = require('net');
var sys = require('sys');

var host = "localhost";
var port = 6600;

this.socket = net.createConnection(port, host);

this.socket.setEncoding('UTF-8');

var writeit = function() {
  this.socket.write("status" + "\n");
}

var self = this;
this.socket.addListener('connect', function() { writeit.call(self); });

this.socket.addListener('data', function(data) {
  for (var i = 0, len = data.length; i<len; i++) {
    console.log(data.substr(i,1) + ': ' + data.charCodeAt(i));
  }
});
